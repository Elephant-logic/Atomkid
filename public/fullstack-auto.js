(() => {
  'use strict';
  if (typeof window === 'undefined' || typeof document === 'undefined') return;

  const KEY = 'atomos-fullstack-auto-v1';
  let state = { running:false, paused:false, stopRequested:false, step:0, maxSteps:6, updates:[] };
  try { state = { ...state, ...JSON.parse(localStorage.getItem(KEY) || '{}'), running:false, paused:false, stopRequested:false }; } catch {}

  const $ = id => document.getElementById(id);
  const esc = value => String(value ?? '').replaceAll('&','&amp;').replaceAll('<','&lt;').replaceAll('>','&gt;');
  const slug = value => String(value || 'atomos-app').toLowerCase().replace(/[^a-z0-9]+/g,'-').replace(/^-|-$/g,'') || 'atomos-app';
  const api = () => window.AtomOSFullStack;
  const project = () => api()?.project?.();

  function save() {
    localStorage.setItem(KEY, JSON.stringify({ ...state, running:false, paused:false, stopRequested:false }));
  }
  function logUpdate(text) {
    state.updates.push(text);
    state.updates = state.updates.slice(-30);
    save();
    renderStatus();
    if (typeof log === 'function') log(text, text.startsWith('✓') ? 'ok' : text.startsWith('✗') ? 'bad' : '');
  }
  function renderStatus() {
    const status = $('fullstackAutoStatus');
    if (status) status.textContent = state.running ? `Auto build running — step ${state.step} of ${state.maxSteps}${state.paused ? ' · paused' : ''}` : 'Auto build is stopped.';
    const updates = $('fullstackAutoUpdates');
    if (updates) updates.innerHTML = state.updates.slice(-10).map(x => `<div>${esc(x)}</div>`).join('');
    const start = $('fullstackAutoStart'); if (start) start.disabled = state.running;
    const pause = $('fullstackAutoPause'); if (pause) { pause.disabled = !state.running; pause.textContent = state.paused ? 'Resume' : 'Pause'; }
    const stop = $('fullstackAutoStop'); if (stop) stop.disabled = !state.running;
    const download = $('fullstackDownloadApp'); if (download) download.disabled = state.running || !project();
  }

  function crc32(bytes) {
    let crc = -1;
    for (const byte of bytes) {
      crc ^= byte;
      for (let i=0;i<8;i++) crc = (crc >>> 1) ^ (0xEDB88320 & -(crc & 1));
    }
    return (crc ^ -1) >>> 0;
  }
  const u16 = n => new Uint8Array([n & 255,(n>>>8)&255]);
  const u32 = n => new Uint8Array([n&255,(n>>>8)&255,(n>>>16)&255,(n>>>24)&255]);
  function join(parts) {
    const out = new Uint8Array(parts.reduce((n,x)=>n+x.length,0));
    let offset=0; for (const part of parts) { out.set(part,offset); offset += part.length; }
    return out;
  }
  function zip(entries) {
    const encoder = new TextEncoder(), locals=[], centrals=[];
    let offset=0;
    for (const entry of entries) {
      const name=encoder.encode(entry.name), data=encoder.encode(String(entry.data ?? '')), crc=crc32(data);
      const local=join([u32(0x04034b50),u16(20),u16(0),u16(0),u16(0),u16(0),u32(crc),u32(data.length),u32(data.length),u16(name.length),u16(0),name,data]);
      locals.push(local);
      centrals.push(join([u32(0x02014b50),u16(20),u16(20),u16(0),u16(0),u16(0),u16(0),u32(crc),u32(data.length),u32(data.length),u16(name.length),u16(0),u16(0),u16(0),u16(0),u32(0),u32(offset),name]));
      offset += local.length;
    }
    const centralSize = centrals.reduce((n,x)=>n+x.length,0);
    return new Blob([join([...locals,...centrals,u32(0x06054b50),u16(0),u16(0),u16(entries.length),u16(entries.length),u32(centralSize),u32(offset),u16(0)])], { type:'application/zip' });
  }
  function downloadApp() {
    const p = project();
    if (!p || state.running) return;
    const root = `${slug(p.title)}/`;
    const backend = p.files.find(file => file.path.startsWith('backend/'))?.path || 'backend/app.py';
    const entries = p.files.map(file => ({ name:root+file.path, data:file.content }));
    entries.push({ name:root+'README.md', data:`# ${p.title}\n\nGenerated by AtomOS.\n\n## Run\n\n1. Install Python 3.\n2. Run \`python ${backend} serve\`.\n3. Open \`frontend/index.html\`.\n` });
    entries.push({ name:root+'start.sh', data:`#!/bin/sh\npython3 "${backend}" serve &\npython3 -m webbrowser "frontend/index.html"\nwait\n` });
    entries.push({ name:root+'start.bat', data:`@echo off\nstart "" python "${backend}" serve\nstart "" "frontend\\index.html"\n` });
    entries.push({ name:root+'project.atomos.json', data:JSON.stringify(p,null,2) });
    const link=document.createElement('a'); link.href=URL.createObjectURL(zip(entries)); link.download=`${slug(p.title)}-v${p.version}.zip`; link.click();
    setTimeout(()=>URL.revokeObjectURL(link.href),1500);
    logUpdate(`✓ Downloaded runnable app package for version ${p.version}`);
  }

  async function startAuto() {
    if (state.running || !project() || !api()?.buildNext) return;
    state.maxSteps = Math.max(1,Math.min(20,Number($('fullstackAutoMax')?.value)||6));
    state.running=true; state.paused=false; state.stopRequested=false; state.step=0;
    logUpdate(`▶ Auto build started with a ${state.maxSteps}-step limit`);
    while (state.running && state.step < state.maxSteps) {
      if (state.stopRequested) break;
      while (state.paused && !state.stopRequested) await new Promise(resolve=>setTimeout(resolve,300));
      if (state.stopRequested) break;
      const before = project()?.version;
      state.step += 1; renderStatus();
      logUpdate(`→ Step ${state.step}: building the next suggested capability`);
      await api().buildNext();
      const after = project()?.version;
      if (!after || after === before) { logUpdate('✗ Auto build stopped because the project did not advance'); break; }
      logUpdate(`✓ Step ${state.step}: project advanced to version ${after}`);
    }
    const reason = state.stopRequested ? 'stopped by user' : state.step >= state.maxSteps ? 'reached its step limit' : 'finished';
    state.running=false; state.paused=false; state.stopRequested=false;
    logUpdate(`■ Auto build ${reason} at version ${project()?.version || '?'}`);
  }
  function pauseAuto() {
    if (!state.running) return;
    state.paused = !state.paused;
    logUpdate(state.paused ? 'Ⅱ Auto build paused' : '▶ Auto build resumed');
  }
  function stopAuto() {
    if (!state.running) return;
    state.stopRequested=true; state.paused=false;
    logUpdate('■ Stop requested; the current generation will finish first');
  }

  function mount() {
    const card = document.querySelector('[data-fullstack-project="true"] .library-card');
    if (!card || $('fullstackAutomation')) return false;
    const panel=document.createElement('div'); panel.id='fullstackAutomation'; panel.style.marginTop='12px';
    panel.innerHTML=`<h3>Automatic project build</h3><p class="muted">Builds one suggested feature at a time, saves each project version and stops on an error or at the step limit.</p><div class="row"><button id="fullstackAutoStart" class="good">Auto build</button><button id="fullstackAutoPause">Pause</button><button id="fullstackAutoStop">Stop</button><button id="fullstackDownloadApp" class="good">Download app</button></div><div class="row"><label class="muted">Maximum steps <input id="fullstackAutoMax" type="number" min="1" max="20" value="${state.maxSteps}" style="width:70px"></label></div><div id="fullstackAutoStatus" class="muted" style="margin-top:8px"></div><div id="fullstackAutoUpdates" class="muted" style="margin-top:8px"></div>`;
    card.appendChild(panel);
    $('fullstackAutoStart').onclick=startAuto;
    $('fullstackAutoPause').onclick=pauseAuto;
    $('fullstackAutoStop').onclick=stopAuto;
    $('fullstackDownloadApp').onclick=downloadApp;
    $('fullstackAutoMax').onchange=event=>{state.maxSteps=Math.max(1,Math.min(20,Number(event.target.value)||6));save();};
    renderStatus();
    return true;
  }

  const observer=new MutationObserver(mount); observer.observe(document.documentElement,{childList:true,subtree:true});
  const timer=setInterval(()=>{ if (api() && mount()) clearInterval(timer); },300);
  mount();
  window.AtomOSFullStackAuto={startAuto,pauseAuto,stopAuto,downloadApp,state:()=>({...state})};
})();